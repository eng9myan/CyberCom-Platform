import uuid
from django.db import models
from platform.common.models import BaseModel


class MedicalRecordAccess(BaseModel):
    RECORD_TYPE_CHOICES = [
        ('diagnosis', 'Diagnosis'),
        ('condition', 'Condition'),
        ('allergy', 'Allergy'),
        ('immunization', 'Immunization'),
        ('care_plan', 'Care Plan'),
        ('clinical_note', 'Clinical Note'),
        ('discharge_summary', 'Discharge Summary'),
        ('visit_history', 'Visit History'),
        ('document', 'Document'),
    ]
    ACCESS_TYPE_CHOICES = [
        ('view', 'View'),
        ('download', 'Download'),
        ('share', 'Share'),
    ]

    account_id = models.UUIDField(db_index=True)
    patient_id = models.UUIDField(db_index=True)
    record_type = models.CharField(max_length=30, choices=RECORD_TYPE_CHOICES)
    record_id = models.UUIDField(db_index=True)
    access_type = models.CharField(
        max_length=20, choices=ACCESS_TYPE_CHOICES, default='view'
    )
    accessed_at = models.DateTimeField(auto_now_add=True)
    access_context = models.CharField(max_length=100, blank=True)

    class Meta:
        db_table = 'cymed_portal_record_access'
        indexes = [
            models.Index(fields=['account_id', 'record_type']),
            models.Index(fields=['patient_id', 'accessed_at']),
        ]

    def __str__(self):
        return f"RecordAccess {self.id} - {self.account_id} [{self.record_type}:{self.access_type}]"


class SharedRecord(BaseModel):
    SHARED_WITH_TYPE_CHOICES = [
        ('provider', 'Provider'),
        ('family_member', 'Family Member'),
        ('employer', 'Employer'),
        ('insurer', 'Insurer'),
        ('other', 'Other'),
    ]

    account_id = models.UUIDField(db_index=True)
    patient_id = models.UUIDField(db_index=True)
    record_type = models.CharField(max_length=50)
    record_id = models.UUIDField()
    record_title = models.CharField(max_length=255)
    shared_with_type = models.CharField(max_length=20, choices=SHARED_WITH_TYPE_CHOICES)
    shared_with_name = models.CharField(max_length=255, blank=True)
    shared_with_email = models.EmailField(blank=True)
    share_token = models.CharField(max_length=255, unique=True, db_index=True)
    valid_until = models.DateTimeField(null=True, blank=True)
    is_password_protected = models.BooleanField(default=False)
    access_count = models.PositiveIntegerField(default=0)
    max_access_count = models.PositiveSmallIntegerField(null=True, blank=True)
    is_revoked = models.BooleanField(default=False)
    revoked_at = models.DateTimeField(null=True, blank=True)

    class Meta:
        db_table = 'cymed_portal_shared_records'
        indexes = [
            models.Index(fields=['account_id', 'is_revoked']),
        ]

    def __str__(self):
        return f"SharedRecord {self.id} - {self.record_title} [{self.shared_with_type}]"


class RecordDownloadHistory(BaseModel):
    DOWNLOAD_FORMAT_CHOICES = [
        ('pdf', 'PDF'),
        ('json', 'JSON'),
        ('fhir', 'FHIR'),
        ('csv', 'CSV'),
    ]

    account_id = models.UUIDField(db_index=True)
    patient_id = models.UUIDField(db_index=True)
    record_type = models.CharField(max_length=50)
    record_id = models.UUIDField()
    record_title = models.CharField(max_length=255)
    download_format = models.CharField(
        max_length=10, choices=DOWNLOAD_FORMAT_CHOICES, default='pdf'
    )
    downloaded_at = models.DateTimeField(auto_now_add=True)
    file_size_bytes = models.PositiveBigIntegerField(default=0)
    ip_address = models.GenericIPAddressField(null=True, blank=True)

    class Meta:
        db_table = 'cymed_portal_download_history'
        indexes = [
            models.Index(fields=['account_id', 'downloaded_at']),
        ]

    def __str__(self):
        return f"DownloadHistory {self.id} - {self.record_title} [{self.download_format}]"


class PatientDocument(BaseModel):
    DOCUMENT_TYPE_CHOICES = [
        ('report', 'Report'),
        ('prescription', 'Prescription'),
        ('insurance_card', 'Insurance Card'),
        ('id_document', 'ID Document'),
        ('lab_result', 'Lab Result'),
        ('imaging', 'Imaging'),
        ('referral', 'Referral'),
        ('discharge_summary', 'Discharge Summary'),
        ('other', 'Other'),
    ]
    SOURCE_CHOICES = [
        ('uploaded_by_patient', 'Uploaded by Patient'),
        ('received_from_provider', 'Received from Provider'),
        ('generated_by_system', 'Generated by System'),
    ]

    account_id = models.UUIDField(db_index=True)
    patient_id = models.UUIDField(db_index=True)
    document_type = models.CharField(max_length=30, choices=DOCUMENT_TYPE_CHOICES)
    title = models.CharField(max_length=255)
    description = models.TextField(blank=True)
    file_name = models.CharField(max_length=255)
    file_url = models.URLField(max_length=2000)
    file_size_bytes = models.PositiveBigIntegerField(default=0)
    file_type = models.CharField(max_length=50)
    document_date = models.DateField(null=True, blank=True)
    source = models.CharField(
        max_length=30, choices=SOURCE_CHOICES, default='uploaded_by_patient'
    )
    provider_name = models.CharField(max_length=255, blank=True)
    is_shared = models.BooleanField(default=False)
    tags = models.JSONField(default=list)

    class Meta:
        db_table = 'cymed_portal_patient_documents'
        indexes = [
            models.Index(fields=['account_id', 'document_type']),
        ]

    def __str__(self):
        return f"PatientDocument {self.title} [{self.document_type}]"
